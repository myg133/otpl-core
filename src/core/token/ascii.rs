/// 换行 \r
pub static LF       : u8 = 10; // 换行 newline \r
/// 回车 \n
pub static CR       : u8 = 13; // 回车 \n
/// TAB \t
pub static TB       : u8 = 23; // TAB \t
/// 空格
pub static SP       : u8 = 32; // 空格
/// 感叹号 !
pub static NOT      : u8 = 33; // !
/// 双引号 "
pub static QUO      : u8 = 34; // "
/// 美元符 $
pub static DLS      : u8 = 36; // $
/// 百分号 %
pub static REM      : u8 = 37; // %
/// 单引号 '
pub static APO      : u8 = 39; // '
/// 乘号 *
pub static MUL      : u8 = 42; // *
/// 减号 -
pub static SUB      : u8 = 45; // -
/// 斜杠 /
pub static SLA      : u8 = 47; // /
/// 小于号 <
pub static LSS      : u8 = 60; // <
/// 等于符号 =
pub static EQS      : u8 = 61; // =
/// 大于符号 >
pub static GTR      : u8 = 62; // >
/// @ 符号
pub static ATS      : u8 = 64; // @

/// 反斜杠 \
pub static BKS      : u8 = 92; // \ 反斜杠 Backslash
/// 下划线 _
pub static UND      : u8 = 95; // _ 下划线 Underscore

